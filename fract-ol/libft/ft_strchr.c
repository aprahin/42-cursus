#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] == c % 256)
			return ((char *)(s + i));
		++i;
	}
	if (c % 256 == '\0')
		return ((char *)(s + i));
	return (0);
}
